package E1;
public class Numeros {
    private int n;
    private double expo;
    private int p=1,mod=0;

    public Numeros(int n) {
        this.n = n;
    }
    public int getN() {
        return n;
    }
    public void setN(int n) {
        this.n = n;
    }  
    public double getExpo() {
        return expo;
    }
    public void setExpo(double expo) {
        this.expo = expo;
    }
    public int getP() {
        return p;
    }
    public void setP(int p) {
        this.p = p;
    }
    public int getMod() {
        return mod;
    }
    public void setMod(int mod) {
        this.mod = mod;
    }      
}
